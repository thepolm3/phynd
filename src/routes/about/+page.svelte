<div class="container">
	<h1>About</h1>
	<p>
		The PHYND website was created as part of a Masters project undertaken at Bournemouth University
		in 2021/22 (<b><a href="https://eprints.bournemouth.ac.uk/38025/">10.18746/BMTH.DATA.00000313</a></b>).
        It sets out to provide an open access photographic database for phytoliths found in
		plants of various ecosystems of the British Isles. The ecosystems explored so far contain
		phytolith photographs of plants from agricultural crops, lowland heath and acid grassland. The
		project is a work in progress and feedback about content, ease of use and applicability should
		be sent to <b>contributions@phynd.online</b>
		<br><br>
		The project is open source, and you can find the source and learn how to contribute on
		<b><a href="https://github.com/thepolm3/phynd">github</a></b>
        <br>
        There is a DOI for this project here: <b><a href="https://zenodo.org/records/8334038">10.5281/zenodo.8334038</a></b>
	</p>

